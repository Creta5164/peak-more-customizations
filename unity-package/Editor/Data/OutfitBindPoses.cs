        private static Matrix4x4[] _value;
        
        public static IReadOnlyCollection<Matrix4x4> Value
            => _value ??= ParseText();
        
        /// <summary>
        /// Fetch the outfit-armature Bindposes matrixes that extracted from a vanilla peak asset.
        /// </summary>
        private static Matrix4x4[] ParseText() {
            
            using var reader = new StringReader(TEXT_VALUE.Trim());
            
            var result = new List<Matrix4x4>();
            
            while (reader.Peek() != -1) {
                
                Matrix4x4 value;
                
                value.m00 = ReadValue();
                value.m01 = ReadValue();
                value.m02 = ReadValue();
                value.m03 = ReadValue();
                
                value.m10 = ReadValue();
                value.m11 = ReadValue();
                value.m12 = ReadValue();
                value.m13 = ReadValue();
                
                value.m20 = ReadValue();
                value.m21 = ReadValue();
                value.m22 = ReadValue();
                value.m23 = ReadValue();
                
                value.m30 = ReadValue();
                value.m31 = ReadValue();
                value.m32 = ReadValue();
                value.m33 = ReadValue();
                
                //The two magic numbers come from the scale of the 'Scout' (0.3307868f) and the 'MainMesh' (0.8258692f) transforms.
                value *= Matrix4x4.Scale(Vector3.one / 0.3307868f / 0.8258692f);
                
                //The 90° rotation cancels out the -90° rotation of the 'MainMesh' transform
                value *= Matrix4x4.Rotate(Quaternion.Euler(90, 0, 0));
                
                result.Add(value);
            }
            
            return result.ToArray();
            
            float ReadValue()
                => float.Parse(reader.ReadLine()[7..]);     //7 is maximum length of element entry to float value.
        }
        
